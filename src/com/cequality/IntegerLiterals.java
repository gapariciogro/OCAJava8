public class IntegerLiterals {
    public static void main(String... args) {
        int decimal = 10;
        int hexadecimal = 0xA;
        int octal = 012;
        int binario = 0b1010;
        
        System.out.println(decimal);
        System.out.println(hexadecimal);
        System.out.println(octal);
        System.out.println(binario);
        
    }
}
